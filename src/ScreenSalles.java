    private void Fill_tableDepart(){

    try{
            con = DriverManager.getConnection("jdbc:mysql://localhost/resturantmanagement","root","");
            String Quary = "SELECT `الاقسام` FROM `depart` ORDER BY `كود القسم`";
            Statement st = con.createStatement();
            rs = st.executeQuery(Quary);
            ResultSetMetaData rsmd = rs.getMetaData();
            model = (DefaultTableModel)table_depart.getModel();
            int cols = rsmd.getColumnCount();
            String[] colName = new String[cols];
            for(int i=0 ; i<cols;i++){
                colName[i] = rsmd.getColumnName(i+1);
            }
            model.setColumnIdentifiers(colName);
            String name;
            while(rs.next()){
                name = rs.getString(1);
                String[] row = {name};
                model.addRow(row);
            }
            st.close();
            con.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }
