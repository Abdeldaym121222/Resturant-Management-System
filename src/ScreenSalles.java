    private void table_departMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_departMouseClicked
        // TODO add your handling code here:
        
         // Table Click
        categort_table.clearSelection();
       int row = this.table_depart.getSelectedRow();
       String click_row = this.table_depart.getModel().getValueAt(row, 0).toString();
       try{
            con = DriverManager.getConnection("jdbc:mysql://localhost/resturantmanagement","root","");
            String Quary = "SELECT `كود الصنف`, `اسم الصنف 1`, `الوحدة`, `سعر التكلفة` FROM `categories` where `القسم`='"+click_row+"'";
            Statement st = con.createStatement();
            rs = st.executeQuery(Quary);
            ResultSetMetaData rsmd = rs.getMetaData();
            model = (DefaultTableModel)categort_table.getModel();
            
            int cols = rsmd.getColumnCount();
            String[] colName = new String[cols];
            for(int i=0 ; i<cols;i++){
                colName[i] = rsmd.getColumnName(i+1);
            }
            model.setColumnIdentifiers(colName);
            String code , name1 , unit , price;
            while(rs.next()){
                code = rs.getString(1);
                name1 = rs.getString(2);
                unit = rs.getString(3);
                price = rs.getString(4);
                String[] row1 = {code , name1 , unit , price};
                model.addRow(row1);
            }
            //st.close();
            //con.close();
            
           // this.categort_table.setModel(DbUtils.resultSetToTableModel(rs));
       }catch(Exception e){
           JOptionPane.showMessageDialog(this, e);
       }
       new client().table_setting(this.categort_table);
    }//GEN-LAST:event_table_departMouseClicked

